    private void mostrardatos() throws SQLException {
        DefaultTableModel modelo=new DefaultTableModel();
        modelo.addColumn("id_product");
        modelo.addColumn("nom_product");
        modelo.addColumn("categoria");
        modelo.addColumn("cantidad");
        modelo.addColumn("vendedor");
        jtableinventario.setModel(modelo);
        String consultasql="select*from inventario";
        String data []= new String [6];
        
        Statement st;
        try{
            st=cn.createStatement();
            ResultSet rs=st.executeQuery(consultasql);
            while(rs.next()){
             data[0]=rs.getString(1);
              data[1]=rs.getString(2);
               data[2]=rs.getString(3);
                data[3]=rs.getString(4);
                 data[4]=rs.getString(5);
            modelo.addRow(data);
            }
        }catch (SQLException e){
            System.out.println("ERROR AL MOSTAR DATOS"+e);
        }
        
        
    }

