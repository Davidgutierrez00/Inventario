    private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed
try{
PreparedStatement ps=cn.prepareStatement("INSERT INTO inventario(nombre_product,categoria,cantidad,vendedor) VALUES (?,?,?,?)");
ps.setString(1,txtnombre_product.getText());
ps.setString(2,txtcategoria.getText());
ps.setString(3,txtcantidad.getText());
ps.setString(4,combovendedor.getSelectedItem().toString());
ps.executeUpdate();
JOptionPane.showMessageDialog(null,"DATOS GUARDADOS CORRECTAMENTE");
mostrardatos();
limpiarentradas();
    }catch (SQLException e){
            System.out.println("ERROR AL REGISTRAR"+e);
}

    }//GEN-LAST:event_btnregistrarActionPerformed

    private void jtableinventarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtableinventarioMouseClicked
       btnregistrar.setEnabled(false);
       btnactualizar.setEnabled(true);
        int fila= this.jtableinventario.getSelectedRow();
        this.txtid.setText(this.jtableinventario.getValueAt(fila,0).toString());
        this.txtnombre_product.setText(this.jtableinventario.getValueAt(fila,1).toString());
        this.txtcategoria.setText(this.jtableinventario.getValueAt(fila,2).toString());
        this.txtcantidad.setText(this.jtableinventario.getValueAt(fila,3).toString());
        this.combovendedor.setSelectedItem(this.jtableinventario.getValueAt(fila,4).toString());
    }//GEN-LAST:event_jtableinventarioMouseClicked

    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
        try{
            PreparedStatement ps=cn.prepareStatement("Update inventario set nombre_product='"+txtnombre_product.getText()+"',categoria='"+txtcategoria.getText()+"',cantidad='"+txtcantidad.getText()+"',vendedor='"+combovendedor.getSelectedItem()+"'where id_product='"+txtid.getText()+"'");
        int indice= ps.executeUpdate();
        if(indice>0){
            JOptionPane.showMessageDialog(rootPane,"DATOS ACTUALIZADOS CORRECTAMENTE");
            mostrardatos();
            limpiarentradas();
        }else{
            JOptionPane.showMessageDialog(null,"NO SELECCIONO FILA");
        }
        } catch(SQLException e){
              JOptionPane.showMessageDialog(null,"ERROR AL ACTUALIZAR DATOS"+e);
        }
    }//GEN-LAST:event_btnactualizarActionPerformed

